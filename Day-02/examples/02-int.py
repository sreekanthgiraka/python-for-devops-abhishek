# Integer variables
num1 = 13
num2 = 5

# Integer Division o/p is 2
result1 = num1 // num2
print("Integer Division:", result1)
 
# Modulus (Remainder)  
result2 = num1 % num2
print("Modulus (Remainder):", result2)  ## o/p is 3


result2 = num1 / num2
print("Regular division:", result2) # Below divison gives exact value 2.6

# Absolute Value
result3 = abs(-7)
print("Absolute Value:", result3)
